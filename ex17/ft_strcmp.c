int		ft_strcmp(char *s1, char *s2)
{
	int		x;
	int		y;

	x = 0;
	y = 0;
	while (s1[x] == s2[y] && s1[x] != '\0' && s2[y] != '\0')
	{
		++x;
		++y;
	}
	return (s1[x] - s2[y]);
}
